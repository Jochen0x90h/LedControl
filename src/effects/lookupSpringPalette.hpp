inline Color<int> lookupSpringPalette(int x) {
	int r, g, b;
	if (x < 0x201) {
		if (x < 0x100) {
			// 0x0 - 0xff
			r = 0 + ((x - 0x0) * 2293 >> 8);
			g = 3358 + ((x - 0x0) * 450 >> 8);
			b = 2375 - ((x - 0x0) * 82 >> 8);
		} else {
			// 0x100 - 0x200
			r = 2293 + ((x - 0x100) * 1393) / 257;
			g = 3808 - ((x - 0x100) * 122) / 257;
			b = 2293;
		}
	} else {
		if (x < 0x300) {
			// 0x201 - 0x2ff
			r = 3686;
			g = 3686;
			b = 2293 - ((x - 0x201) * 2293) / 255;
		} else {
			// 0x300 - 0x3ff
			r = 3686 - ((x - 0x300) * 3686 >> 8);
			g = 3686 - ((x - 0x300) * 328 >> 8);
			b = 0 + ((x - 0x300) * 2375 >> 8);
		}
	}
	return {r, g, b};
}
