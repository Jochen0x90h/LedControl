inline Color<int> lookupAutumnPalette(int x) {
	int r, g, b;
	if (x < 0x100) {
		if (x < 0x80) {
			// 0x0 - 0x7f
			r = 2662 - ((x - 0x0) * 1843 >> 7);
			g = 655 + ((x - 0x0) * 2621 >> 7);
			b = 655 + ((x - 0x0) * 164 >> 7);
		} else {
			// 0x80 - 0xff
			r = 819 + ((x - 0x80) * 2867 >> 7);
			g = 3276 + ((x - 0x80) * 410 >> 7);
			b = 819 - ((x - 0x80) * 819 >> 7);
		}
	} else {
		if (x < 0x200) {
			// 0x100 - 0x1ff
			r = 3686;
			g = 3686 - ((x - 0x100) * 1925 >> 8);
			b = 0;
		} else {
			if (x < 0x300) {
				// 0x200 - 0x2ff
				r = 3686 + ((x - 0x200) * 409 >> 8);
				g = 1761 - ((x - 0x200) * 1761 >> 8);
				b = 0;
			} else {
				// 0x300 - 0x3ff
				r = 4095 - ((x - 0x300) * 1433 >> 8);
				g = 0 + ((x - 0x300) * 655 >> 8);
				b = 0 + ((x - 0x300) * 655 >> 8);
			}
		}
	}
	return {r, g, b};
}
